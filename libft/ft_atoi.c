int	ft_atoi(const char *str)
{
	int a;
	int m;

	a = 0;
	m = 1;
	while (*str == '\t' || *str == '\n' || *str == '\v' || *str == '\f'
			|| *str == '\r' || *str == ' ')
		str++;
	if (*str == '-')
	{
		m = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		a *= 10;
		a += *str - '0';
		str++;
	}
	return (a * m);
}
