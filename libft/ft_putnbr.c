#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 2147483647 || n > -2147483648)
	{
		if (n < 0)
		{
			ft_putchar('-');
			if (n == -2147483648)
			{
				n = -214748364;
				ft_putnbr(-n);
				ft_putnbr(8);
			}
			else
				ft_putnbr(-n);
		}
		else if (n >= 10)
		{
			ft_putnbr(n / 10);
			ft_putchar(n % 10 + '0');
		}
		else
			ft_putchar(n + '0');
	}
}
